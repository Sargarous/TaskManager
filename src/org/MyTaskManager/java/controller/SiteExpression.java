package by.it.kruglenja.TaskManager.java.controller;

public class SiteExpression extends Exception{
    public SiteExpression() {
    }

    public SiteExpression(String message) {
        super(message);
    }

    public SiteExpression(String message, Throwable cause) {
        super(message, cause);
    }

    public SiteExpression(Throwable cause) {
        super(cause);
    }

    public SiteExpression(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
